// 数据类型示例
fn main() {
    // 整数类型
    let integer: i32 = 42;
    println!("整数类型: {}", integer);

    // 浮点数类型
    let float: f64 = 3.14;
    println!("浮点数类型: {}", float);

    // 布尔类型
    let boolean: bool = true;
    println!("布尔类型: {}", boolean);

    // 字符类型
    let character: char = 'A';
    println!("字符类型: {}", character);
}